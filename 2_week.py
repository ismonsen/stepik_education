x = len(a)
if x == 1:
    b = a
else:
    b = [0 for i in range(x)]
    for i in range(0, len(a)):
        if i == 0:
            b[i] = a[i + 1] + a[x - 1]
        elif i == x - 1:
            b[i] = a[i - 1] + a[0]
        else:
            b[i] = a[i - 1] + a[i + 1]
