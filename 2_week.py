# Задача 2.1_1
# Напишите программу, которая считывает со стандартного ввода целые числа, по одному числу
# в строке, и после первого введенного нуля выводит сумму полученных на вход чисел.
s = 0
a = int(input())
while a != 0:
    s += a
    a = int(input())
print(s)

# Задача 2.1_2
# Программа должна считывать размеры команд (два положительных целых числа aa и bb, каждое число вводится на
# отдельной строке) и выводить наименьшее число dd, которое делится на оба этих числа без остатка.
a, b = int(input()), int(input())
d = a
while d % b != 0:
    d += a
print(d)

# Задача 2.2
# Напишите программу, которая считывает целые числа с консоли по одному числу в строке.
# Для каждого введённого числа проверить:
# если число меньше 10, то пропускаем это число;
# если число больше 100, то прекращаем считывать числа;
# в остальных случаях вывести это число обратно на консоль в отдельной строке.
a = int(input())
while a <= 100:
    if a > 100:
        break
    elif a < 10:
        a = int(input())
        continue
    else:
        print(a)
        a = int(input())

