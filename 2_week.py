# Задача 2.4_2
# Программа по кодированию: например, s = 'aaaabbсaa' преобразуется в 'a4b2с1a2'
s, s1, count = input(), '', 1
for i in range(len(s)):
    a = s[i]
    if i == len(s) - 1:
        s1 += a + str(count)
        break
    if s[i + 1] == s[i]:
        count += 1
    else:
        s1 += a + str(count)
        count = 1
    i += 1
print(s1)

